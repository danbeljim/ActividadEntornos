package metodoSepararVariablesTemporales;

public class MetodoResuelto {

    void variablesTemporales(double pi, double r){
        double perimetroCirculo = 2 * pi * r;
        System.out.println(perimetroCirculo);
        double areaCirculo = pi * r * r;
        System.out.println(areaCirculo);
    }

}
